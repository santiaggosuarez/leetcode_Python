# Función para obtener los primeros 7 números pares
def lista_pares():
  lista = []
  num = 0

  while len(lista) < 7:
    if num % 2 == 0:
      lista.append(num)
    num += 1

  return lista

if __name__ == "__main__":
  print("PROBLEMA 1\nLista con los primeros 7 números pares:\n")
  print(lista_pares())
